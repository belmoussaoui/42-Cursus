int	ft_str_is_numeric(char *str)
{
	int	res;
	int	i;

	res = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ('0' <= str[i] && str[i] <= '9')
		{
			i++;
		}
		else
		{
			res = 0;
			break ;
		}
	}
	return (res);
}
