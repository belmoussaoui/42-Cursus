int	ft_str_is_alpha(char *str)
{
	int	res;
	int	i;

	i = 0;
	res = 1;
	while (str[i] != '\0')
	{
		if (('A' <= str[i] && str[i] <= 'Z')
			|| ('a' <= str[i] && str[i] <= 'z'))
		{
			i++;
		}
		else
		{
			res = 0;
			break ;
		}
	}
	return (res);
}
