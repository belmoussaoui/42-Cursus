int	ft_is_prime(int nb)
{
	int	div;

	if (nb <= 1)
		return (0);
	div = 2;
	while (div < (nb / 2) + 1)
	{
		if (nb % div == 0)
			return (0);
		div++;
	}
	return (1);
}
