int	ft_sqrt(int nb)
{
	int	i;
	int	res;

	if (nb <= 1)
		return (0);
	i = 0;
	while (res < nb && i < 46340)
	{
		i++;
		res = i * i;
	}
	if (res > nb)
		return (0);
	return (res);
}
