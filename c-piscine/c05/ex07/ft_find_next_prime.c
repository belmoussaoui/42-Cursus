int	ft_is_next_prime(int nb)
{
	int	div;
	int	res;

	if (nb <= 1)
		nb = 2;
	while (1)
	{
		res = 1;
		div = 2;
		while (div < (nb / 2) + 1)
		{
			if (nb % div == 0)
				res = 0;
			div++;
		}
		if (res == 1)
			return (nb);
		nb++;
	}
	return (0);
}
