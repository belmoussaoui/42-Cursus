int	clz(unsigned int i)
{
	int				z;
	unsigned int	n;

	z = 0;
	n = 1 << ((sizeof(unsigned int) << 3) - 1);
	while ((!(n & i)) && n > 0)
	{
		n >>= 1;
		z++;
	}
	return (z);
}
